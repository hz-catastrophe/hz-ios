import Foundation
import UIKit

extension UIColor {

  class func HZMainColor() -> UIColor {
    return UIColor(red: 193/255, green: 38/255, blue: 0/255, alpha: 1.0)
  }

  class func HZLightColor() -> UIColor {
    return UIColor(white: 0.8, alpha: 1.0)
  }

}